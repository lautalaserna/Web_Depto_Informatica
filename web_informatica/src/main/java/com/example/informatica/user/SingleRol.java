package com.example.informatica.user;

public class SingleRol {
	String rol;
	boolean value;
	public SingleRol(String rol, boolean value) {
		super();
		this.rol = rol;
		this.value = value;
	}
	public String getRol() {
		return rol;
	}
	public void setRol(String rol) {
		this.rol = rol;
	}
	public boolean isValue() {
		return value;
	}
	public void setValue(boolean value) {
		this.value = value;
	}

}
